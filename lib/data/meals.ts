export interface Ingredient {
  name: string;
  quantity: string;    // e.g. "2", "100", "1/2"
  unit: string;        // e.g. "cups", "grams", "tbsp", "whole"
  category: string;    // e.g. "produce", "dairy", "protein", "grains", "pantry"
}

export interface Meal {
  id: string;
  name: string;
  category: "breakfast" | "lunch" | "dinner" | "snack";
  calories: number;
  protein: number;       // grams
  carbs: number;         // grams
  fat: number;           // grams
  costPerServing: number; // in USD
  prepTime: number;       // minutes
  dietTags: string[];    // e.g. ["vegetarian", "gluten-free"]
  ingredients: Ingredient[];
}

